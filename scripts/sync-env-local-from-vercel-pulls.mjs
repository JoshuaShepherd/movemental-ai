/**
 * Build `.env.local` from `vercel env pull` outputs (not committed).
 *
 *   pnpm exec vercel env pull .env.vercel.production --environment production --yes
 *   pnpm exec vercel env pull .env.vercel.preview --environment preview --yes
 *   pnpm exec vercel env pull .env.vercel.development --environment development --yes
 *   node scripts/sync-env-local-from-vercel-pulls.mjs
 *
 * Merge order (later wins if value is non-empty after trim): production → preview → development.
 * Supabase: if `NEXT_PUBLIC_SUPABASE_ANON_KEY` is empty but
 * `NEXT_PUBLIC_SUPABASE_PUBLISHABLE_DEFAULT_KEY` exists, copy it (new Supabase key names).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parse } from "dotenv";

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");

const FILES = [
  ".env.vercel.production",
  ".env.vercel.preview",
  ".env.vercel.development",
];

function loadParsed(name) {
  const p = path.join(ROOT, name);
  if (!fs.existsSync(p)) return {};
  return parse(fs.readFileSync(p, "utf8"));
}

function isNonEmpty(v) {
  return v != null && String(v).trim().length > 0;
}

/** @type {Record<string, string>} */
let merged = {};
for (const f of FILES) {
  const chunk = loadParsed(f);
  for (const [k, v] of Object.entries(chunk)) {
    if (isNonEmpty(v)) merged[k] = String(v);
  }
}

if (
  !isNonEmpty(merged.NEXT_PUBLIC_SUPABASE_ANON_KEY) &&
  isNonEmpty(merged.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_DEFAULT_KEY)
) {
  merged.NEXT_PUBLIC_SUPABASE_ANON_KEY =
    merged.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_DEFAULT_KEY;
}

const header = `# Generated by scripts/sync-env-local-from-vercel-pulls.mjs
# Sources (non-empty wins last): ${FILES.join(", ")}
# Re-pull: pnpm exec vercel env pull .env.vercel.<env> --environment <env> --yes

`;

const keys = Object.keys(merged).sort((a, b) => a.localeCompare(b));
let body = "";
for (const key of keys) {
  const val = merged[key];
  const needsQuotes = /[\s"'`#]/.test(val) || val.includes("\n");
  body += needsQuotes
    ? `${key}="${val.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"\n`
    : `${key}=${val}\n`;
}

const outPath = path.join(ROOT, ".env.local");
fs.writeFileSync(outPath, header + body, "utf8");
console.log(`Wrote ${keys.length} vars to .env.local`);
