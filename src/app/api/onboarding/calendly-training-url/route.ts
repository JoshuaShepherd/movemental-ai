import { NextRequest, NextResponse } from "next/server";

import { createClient } from "@/lib/supabase/server";
import { resolveActiveOrganizationId } from "@/lib/services/onboarding/onboarding.service";
import { getCalendlyTrainingUrlForOrganization } from "@/lib/scheduling/calendly-training-url";

export async function GET(request: NextRequest) {
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();
  if (!user?.id) {
    return NextResponse.json(
      { error: { code: "unauthorized", message: "Sign in required." } },
      { status: 401 },
    );
  }

  const organizationSlug =
    request.nextUrl.searchParams.get("org") ?? undefined;
  const resolved = await resolveActiveOrganizationId(user.id, organizationSlug);
  if (!resolved.success) {
    return NextResponse.json({ error: resolved.error }, { status: 400 });
  }

  const url = await getCalendlyTrainingUrlForOrganization(resolved.data.organizationId);
  return NextResponse.json({ url });
}
